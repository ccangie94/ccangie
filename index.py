class Personas (Object):
    nombre = ""
    edad = 0
    estatura = 0.0
    genero = ""

def __init__(self, nombre, edad, estatura, genero):
    self.nombre=nombre
    self.edad=edad
    self.estatura=estatura
    self.genero=genero

def caminar(self):
    self.Caminar

def hablar(self):
    self.Hablar

def saltar(self):
    self.Saltar

def es_mayor_de_edad(self):
    self.Es_mayor_de_edad